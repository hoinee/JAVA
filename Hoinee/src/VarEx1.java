
public class VarEx1 {
	public static void main(String[] args) {
		
			/*
			  변수 값을 저장할 수 있는 메모리상의 공간을 의미
			 */
			int year = 2017; // year 이라는 변수를 선언
			int age = 26;
			
			System.out.println(year);
			System.out.println(age);
			
			year = age + 2000;
			age = age + 1;
			
			System.out.println(year);
			System.out.println(age);
	}
}
